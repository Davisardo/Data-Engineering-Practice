def square(number):
    """Mengembalikan kuadrat dari angka yang diberikan."""
    return number**2


def double(number):
    """Mengembalikan dua kali lipat dari angka yang diberikan."""
    return number * 2


def add(a, b):
    """Mengembalikan hasil penjumlahan dari dua angka yang diberikan."""
    return a + b
